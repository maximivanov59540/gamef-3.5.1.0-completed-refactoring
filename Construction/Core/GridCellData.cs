using UnityEngine;

/// <summary>
/// Структура ("контейнер") данных для ОДНОЙ ячейки сетки.
/// Хранит ссылки на то, что может быть в этой ячейке.
/// </summary>
public struct GridCellData
{
    /// <summary>
    /// Ссылка на здание, занимающее эту клетку.
    /// (null, если здания нет)
    /// </summary>
    public BuildingIdentity building;

    /// <summary>
    /// Ссылка на компонент дороги, лежащий в этой клетке.
    /// (null, если дороги нет)
    /// </summary>
    public RoadTile road;

    // --- ⬇️ НОВЫЙ КОД (Шаг 5: Часть А) ⬇️ ---
    /// <summary>
    /// Ссылка на "модуль" (напр., Поле Фермы), занимающий эту клетку.
    /// (null, если модуля нет)
    /// </summary>
    public BuildingModule module;
    // --- ⬆️ КОНЕЦ НОВОГО КОДА ⬆️ ---

    // --- ⬇️ НОВЫЙ КОД (Шаг 5: Часть Б) ⬇️ ---
    /// <summary>
    /// Ссылка на "зону" (напр., Монастырь), к которой принадлежит эта клетка.
    /// (null, если клетка не "зонирована")
    /// </summary>
    public ZonedArea parentZone;
    // --- ⬆️ КОНЕЦ НОВОГО КОДА ⬆️ ---
}